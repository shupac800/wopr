// Continent coastline outlines
// Loads Natural Earth 50m land data from CDN, falls back to embedded data

// Will be populated by loadCoastlines() — array of arrays of [lat, lon] pairs
let CONTINENT_OUTLINES = {};

// Load Natural Earth TopoJSON coastlines
async function loadCoastlines() {
  try {
    const resp = await fetch('https://cdn.jsdelivr.net/npm/world-atlas@2/land-50m.json');
    if (!resp.ok) throw new Error('fetch failed');
    const topo = await resp.json();
    const geo = topojson.feature(topo, topo.objects.land);

    CONTINENT_OUTLINES = {};
    let idx = 0;
    for (const feature of geo.features) {
      const geom = feature.geometry;
      const polys = geom.type === 'MultiPolygon' ? geom.coordinates : [geom.coordinates];
      for (const poly of polys) {
        for (const ring of poly) {
          // GeoJSON is [lon, lat], we store [lat, lon]
          CONTINENT_OUTLINES['land_' + idx] = ring.map(([lon, lat]) => [lat, lon]);
          idx++;
        }
      }
    }
    console.log(`Loaded ${idx} coastline segments from Natural Earth`);
  } catch (e) {
    console.warn('Could not load Natural Earth data, using fallback:', e.message);
    CONTINENT_OUTLINES = FALLBACK_OUTLINES;
  }
}

